package com.li.literal;

public class LiteralDemo {
    public static void main(String[] args) {
//        整数
        System.out.println(666);
//        小数
        System.out.println(19.6);
//        字符，必须用单引号括起来
        System.out.println(' ');
        System.out.println('a');
//        字符串，必须用双引号括起来
        System.out.println("askd");
//        \n 换行 \t tab一个缩进
    }
}
